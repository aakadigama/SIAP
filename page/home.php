        <h2><a href="#">Universitas Paramadina</a></h2>
        <p>Pada tahun 1994, dua yayasan, Yayasan Wakaf Paramadina dengan ketua Prof. Dr. Nurcholish Madjid dan Yayasan Pondok Mulya, yang masing-masing bergerak di bidang pendidikan, sepakat untuk bekerjasama untuk mendirikan sebuah perguruan tinggi.</p>
        <p>Kesepakatan tersebut diwujudkan dengan didirikannya Yayasan ParamadinaMulya pada tahun 1995. Sebagai realisasi dari gagasan tersebut, maka pada tanggal 10 Januari 1998 diresmikan berdirinya Universitas ParamadinaMulya, yang diharapkan mampu: menyempurnakan konsep perguruan tinggi yang sudah ada, menghasilkan lulusan yang memiliki keahlian yang diperlukan oleh bangsa Indonesia, yang mempunyai kompetensi riset, semangat kewirausahaan yang menjiwai etika keislaman. menjadi wahana pusat kebudayaan dan peradaban mampu mengembangkan kepribadian mahasiswa memiliki fitrahnya sebagai universitas yang universal, sehingga mampu menyebut dirinya bertaraf internasional.</p>
        <p>Pada awal berdirinya, Universitas ParamadinaMulya mempunyai empat program studi, yaitu Teknik Informatika, Disain Produk, Manajemen, dan Falsafah dan Agama. Pada tahun akademik 1999 / 2000 dibuka program pasca sarjana, diantaranya Magister Ilmu Agama Islam, dan program studi sarjana Desain Grafis / Komunikasi Visual, Ilmu Komunikasi dan Hubungan Internasional. Sedangkan pada tahun akademik 2000 / 2001, dibuka program studi Psikologi. Pada tanggal 20 Maret 2002 pengelola Universitas diserahkan sepenuhnya kepada Yayasan Wakaf Paramadina, sedangkan nama universitas disesuaikan dari Universitas ParamadinaMulya menjadi Universitas Paramadina.</p>
